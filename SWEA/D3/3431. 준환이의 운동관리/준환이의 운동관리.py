# 준환이의 운동관리

# 몸 유지를 위해 1주일에 L분 이상 U분 이하의 운동
# 1. 제한되어 있는 시간을 넘은 운동을 할 것인지
# 2. 그게 아니라면 몇 분 더 운동을 해야 제한을 맞출 수 있는지 출력

# 세 정수 L 이상, U 이하, X는 한 운동 시간

T = int(input())
for t in range(1,T+1):
    L,U,X = map(int, input().split())

    # X가 L과 U 사이에 있는지 확인
    if L <= X <= U:
        # 운동 시간이 제한 내에 있는 경우 추가 운동 필요 없음
        print(f"#{t} 0")
    elif X < L:
        # 운동 시간이 부족한 경우 필요한 추가 운동 시간 계산
        print(f"#{t} {L - X}")
    else:  # X > U
        # 운동 시간이 초과한 경우 -1 출력
        print(f"#{t} -1")



